package com.ahmadgilang.a;

public class Mahasiswa {

	public String nim;
	public String nama;
	public Integer nilaiKehadiran;
	public Integer nilaiMidtest;
	public Integer nilaiUas;

	public Integer nilaiAkhir;
	public String grade;

	public Mahasiswa() {

	}

	public String getNim() {
		return nim;
	}

	public void setNim(String nim) {
		this.nim = nim;
	}

	public String getNama() {
		return nama;
	}

	public void setNama(String nama) {
		this.nama = nama;
	}

	public Integer getNilaiKehadiran() {
		return nilaiKehadiran;
	}

	public void setNilaiKehadiran(Integer nilaiKehadiran) {
		this.nilaiKehadiran = nilaiKehadiran;
	}

	public Integer getNilaiMidtest() {
		return nilaiMidtest;
	}

	public void setNilaiMidtest(Integer nilaiMidtest) {
		this.nilaiMidtest = nilaiMidtest;
	}

	public Integer getNilaiUas() {
		return nilaiUas;
	}

	public void setNilaiUas(Integer nilaiUas) {
		this.nilaiUas = nilaiUas;
	}

	public Integer getNilaiAkhir() {
		return nilaiAkhir;
	}

	public void setNilaiAkhir(Integer nilaiAkhir) {
		this.nilaiAkhir = nilaiAkhir;
	}

	public String getGrade() {
		return grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}

}
